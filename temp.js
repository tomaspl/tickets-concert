function removeDuplicateFamilies(data, exceptions = []) {
  const seenCodes = new Set()
  const result = {}

  for (const [key, family] of Object.entries(data.families)) {
    const { familyCode } = family

    // Si el key está en las excepciones → lo mantenemos siempre
    if (exceptions.includes(key)) {
      result[key] = family
      continue
    }

    // Si no hemos visto este familyCode, lo agregamos
    if (!seenCodes.has(familyCode)) {
      seenCodes.add(familyCode)
      result[key] = family
    }
    // Si ya lo vimos y no es excepción → lo omitimos
  }

  // Retornamos un nuevo objeto con la misma estructura
  return { families: result }
}
